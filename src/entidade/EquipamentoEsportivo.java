package entidade;

public class EquipamentoEsportivo {

	
	
	private int idProduto;
	private String esporteRelacionado;
	private String nome;
	private String marca;
	private String modelo;
	private double preco;
	private int quantidade;
	
	
	
	
	
	
	public String getEsporteRelacionado() {
		return esporteRelacionado;
	}
	public void setEsporteRelacionado(String esporteRelacionado) {
		this.esporteRelacionado = esporteRelacionado;
	}
	public int getIdProduto() {
		return idProduto;
	}
	public void setIdProduto(int idProduto) {
		this.idProduto = idProduto;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	} 
	
	
	
	
	
	
	
	
}
